package model.game;

public class Coords {

    /**
     * @return the x
     */
    public Integer getX() {
	return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(Integer x) {
	this.x = x;
    }

    /**
     * @return the y
     */
    public Integer getY() {
	return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(Integer y) {
	this.y = y;
    }
    
    private Integer x;
    private Integer y;
    
}